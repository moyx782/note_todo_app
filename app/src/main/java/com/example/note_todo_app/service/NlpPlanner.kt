class NlpPlanner {
    
    /**
     * Parses natural language input to extract todos and schedule information
     */
    fun parseNaturalLanguage(input: String): List<Todo> {
        val todos = mutableListOf<Todo>()
        
        // Split the input into sentences or phrases
        val segments = splitIntoSegments(input)
        
        for (segment in segments) {
            val todo = parseSegment(segment.trim())
            if (todo != null) {
                todos.add(todo)
            }
        }
        
        return todos
    }
    
    /**
     * Splits input text into smaller segments that can be parsed individually
     */
    private fun splitIntoSegments(input: String): List<String> {
        // Split by common sentence terminators
        val sentenceSplitter = Regex("[.!?。！？\n]")
        var segments = sentenceSplitter.split(input)
        
        // Also consider colon-separated items as potential tasks
        val additionalSegments = mutableListOf<String>()
        for (segment in segments) {
            if (segment.contains(":") || segment.contains("：")) {
                val parts = segment.split(Regex("[:：]"))
                additionalSegments.addAll(parts.filter { it.isNotBlank() })
            } else {
                additionalSegments.add(segment)
            }
        }
        
        return additionalSegments.filter { it.isNotBlank() && it.length > 2 }
    }
    
    /**
     * Parses a single segment of text to create a Todo object
     */
    private fun parseSegment(segment: String): Todo? {
        // Check if this segment contains actionable content
        if (!looksLikeActionableContent(segment)) {
            return null
        }
        
        // Extract task details using various patterns
        val extractedDetails = extractTaskDetails(segment)
        
        // Determine due date from natural language
        val dueDate = extractDueDateFromNaturalLanguage(segment)
        
        // Determine priority based on urgency indicators
        val priority = determinePriorityFromNaturalLanguage(segment)
        
        return Todo(
            id = UUID.randomUUID().toString(),
            title = extractedDetails.title,
            description = extractedDetails.description,
            isCompleted = false,
            priority = priority,
            dueDate = dueDate,
            createdAt = Date(),
            updatedAt = Date(),
            noteId = "" // Will be set when associated with a note
        )
    }
    
    /**
     * Extracts task details like title and description from the segment
     */
    private fun extractTaskDetails(segment: String): TaskDetails {
        var title = segment.trim()
        var description = ""
        
        // Common prefixes that indicate tasks
        val prefixes = listOf(
            "我要", "我需要", "我应该", "我必须", "请帮我", "安排", "计划", 
            "提醒我", "记得", "不要忘记", "务必", "尽快", "马上"
        )
        
        for (prefix in prefixes) {
            if (title.startsWith(prefix)) {
                title = title.substring(prefix.length).trim()
                break
            }
        }
        
        // If there are multiple clauses separated by commas, use first as title, rest as description
        if (title.contains(",")) {
            val parts = title.split(",", limit = 2)
            title = parts[0].trim()
            if (parts.size > 1) {
                description = parts[1].trim()
            }
        }
        
        return TaskDetails(title, description)
    }
    
    /**
     * Attempts to extract a due date from natural language expressions
     */
    private fun extractDueDateFromNaturalLanguage(text: String): String? {
        val lowerText = text.lowercase()
        
        // Handle relative time expressions
        when {
            containsAny(lowerText, listOf("今天", "today", "tonight")) -> {
                return formatDate(Date())
            }
            containsAny(lowerText, listOf("明天", "tomorrow")) -> {
                val tomorrow = Date(System.currentTimeMillis() + 24 * 60 * 60 * 1000)
                return formatDate(tomorrow)
            }
            containsAny(lowerText, listOf("后天", "day after tomorrow")) -> {
                val dayAfterTomorrow = Date(System.currentTimeMillis() + 2 * 24 * 60 * 60 * 1000)
                return formatDate(dayAfterTomorrow)
            }
            containsAny(lowerText, listOf("本周", "this week")) -> {
                // Calculate end of current week (Sunday)
                val calendar = Calendar.getInstance()
                calendar.set(Calendar.DAY_OF_WEEK, Calendar.SUNDAY)
                return formatDate(calendar.time)
            }
            containsAny(lowerText, listOf("下周", "next week")) -> {
                // Calculate start of next week (Monday)
                val calendar = Calendar.getInstance()
                calendar.add(Calendar.WEEK_OF_YEAR, 1)
                calendar.set(Calendar.DAY_OF_WEEK, Calendar.MONDAY)
                return formatDate(calendar.time)
            }
            containsAny(lowerText, listOf("本月", "this month")) -> {
                val calendar = Calendar.getInstance()
                calendar.set(Calendar.DAY_OF_MONTH, calendar.getActualMaximum(Calendar.DAY_OF_MONTH))
                return formatDate(calendar.time)
            }
            containsAny(lowerText, listOf("下个月", "next month")) -> {
                val calendar = Calendar.getInstance()
                calendar.add(Calendar.MONTH, 1)
                return formatDate(calendar.time)
            }
        }
        
        // Look for specific date formats
        val datePatterns = listOf(
            Regex("""(\d{4})[年/-](\d{1,2})[月/-](\d{1,2})[日]?"""), // YYYY-MM-DD format
            Regex("""(\d{1,2})[月/-](\d{1,2})[日]?"""),             // MM-DD format
            Regex("""(\d{4})-(\d{2})-(\d{2})"""),                   // Standard ISO format
            Regex("""(\d{1,2})/(\d{1,2})/(\d{4})"""),               // US format
            Regex("""(\d{1,2}).(\d{1,2}).(\d{4})""")                // European format
        )
        
        for (pattern in datePatterns) {
            val match = pattern.find(text)
            if (match != null) {
                val groups = match.groupValues
                
                // For YYYY-MM-DD or YYYY/MM/DD format
                if (groups.size == 4 && groups[1].length == 4) {
                    return "${groups[1]}-${groups[2].padStart(2, '0')}-${groups[3].padStart(2, '0')}"
                }
                
                // For MM-DD-YYYY or DD/MM/YYYY format
                if (groups.size == 4) {
                    // Assuming MM-DD-YYYY format (common in English)
                    if (text.contains("/")) {
                        // This might be DD/MM/YYYY - need to check which is more reasonable
                        val day = groups[1].toIntOrNull()
                        val month = groups[2].toIntOrNull()
                        
                        if (month != null && day != null) {
                            if (month <= 12 && day <= 31) {
                                return "${groups[3]}-${month.toString().padStart(2, '0')}-${day.toString().padStart(2, '0')}"
                            }
                        }
                    } else {
                        // MM-DD-YYYY format
                        return "${groups[3]}-${groups[1].padStart(2, '0')}-${groups[2].padStart(2, '0')}"
                    }
                }
                
                // For MM-DD or DD/MM format without year
                if (groups.size == 3) {
                    val now = Calendar.getInstance()
                    val year = now.get(Calendar.YEAR)
                    
                    // Assuming MM-DD format
                    return "$year-${groups[1].padStart(2, '0')}-${groups[2].padStart(2, '0')}"
                }
            }
        }
        
        return null
    }
    
    /**
     * Determines priority level based on natural language indicators
     */
    private fun determinePriorityFromNaturalLanguage(text: String): Int {
        val lowerText = text.lowercase()
        
        // High priority indicators
        val highPriorityKeywords = listOf(
            "紧急", "立刻", "马上", "尽快", "立即", "赶紧", "火速", "迫切", 
            "urgent", "asap", "immediately", "right now", "now", "stat"
        )
        
        // Medium priority indicators
        val mediumPriorityKeywords = listOf(
            "重要", "注意", "小心", "认真", "仔细", "优先", "提前",
            "important", "priority", "high priority", "medium priority"
        )
        
        // Check for high priority keywords
        for (keyword in highPriorityKeywords) {
            if (lowerText.contains(keyword.lowercase())) {
                return 2 // High priority
            }
        }
        
        // Check for medium priority keywords
        for (keyword in mediumPriorityKeywords) {
            if (lowerText.contains(keyword.lowercase())) {
                return 1 // Medium priority
            }
        }
        
        return 0 // Low priority
    }
    
